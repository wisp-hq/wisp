export const THEMES: Theme[] = [CLASSIC_LIGHT, CATPPUCCIN_MOCHA, CATPPUCCIN_LATTE, DRACULA, NORD, TOKYO_NIGHT, GRUVBOX, ROSE_PINE, SOLARIZED, SOLARIZED_LIGHT, ONE_DARK, MONOKAI_PRO, GITHUB_DARK, EVERFOREST, SYNTHWAVE];

const BY_KEY: Record<string, Theme> = Object.fromEntries(THEMES.map((t) => [t.key, t]));

export function getTheme(key: string | null | undefined): Theme | null {
  if (!key) {
    return null;
  }

  return BY_KEY[key] ?? null;
}

const CLASSIC_AVATAR = '#475569';

export function getAvatarBackground(themeKey: string | null | undefined): string {
  return getTheme(themeKey)?.avatar ?? CLASSIC_AVATAR;
}
